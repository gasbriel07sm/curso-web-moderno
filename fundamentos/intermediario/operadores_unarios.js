let num1 = 1;
let num2 = 2;

// Incremento
num1++ // acrescenta 1 na variável (pós-fixada, ocorre depois de um comando)
console.log(num1);
++num1 // acrescenta 1 na variável (pré-fixada, ocorre antes de um comando)
console.log(num1)

// Decremento
num1-- // descrementa 1 na variável (pós-fixada, ocorre depois de um comando)
console.log(num1);
--num1 // decrementa 1 na variável (pré-fixada, ocorre antes de um comando)
console.log(num1)

console.log(++num1 === num2--) 